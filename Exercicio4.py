#4.	Crie um programa que converta metros para centímetros.

qtdMetros = float(input("Quantos metros deseja converter? "))
qtdCentimetros = 100

resultadoCentimetros = qtdMetros * qtdCentimetros

print(f" O resultado de {qtdMetros:.2f} metros convertidos em centimentros é {resultadoCentimetros:.2f} centimetros")